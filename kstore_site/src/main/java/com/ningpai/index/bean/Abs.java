package com.ningpai.index.bean;

/**
 * Abs
 * @author Songhl
 */
public class Abs {
    /**
     * Abs的url
     */
    private String abUrl;
    /**
     * 名字
     */
    private String name;
    /**
     * 路径
     */
    private String path;

    public String getAbUrl() {
        return abUrl;
    }

    public void setAbUrl(String abUrl) {
        this.abUrl = abUrl;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }
}
