$(function(){

    $("#coupon-slides").slidesjs({
        play: {
            active: false,
            effect: "fade",
            auto: true,
            interval: 4000,
            pauseOnHover: true,
            restartDelay: 2500
        },
        navigation: {
            active: true,
            effect: "slide"
        },
        pagination: {
            active: true,
            effect: "fade"
        }
    });
    
    $(".item-quan a").click(function(){
        var _this = $(this);
        if(!_this.hasClass("coupon-disabled")) {
            $(".mask, .coupon-dialog").fadeIn();
        };
    });
    $(".close-dia, .cp-cancel").click(function(){
        $(".mask, .coupon-dialog").fadeOut();
    });
    
});